/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
// 方法1:由二叉排序树的特性可以从根节点找到p,q,并将路径记录在两个数组中,然后遍历两个数组,当第一次出现不同的元素时,上一个元素即为最近的公共祖先
class Solution {
    // 方法2:如果遍历二叉排序树,如果当前的结点的值,均大于p,q的值,说明p,q在当前结点的左子树,向左遍历
    //      如果当前的结点均小于p,q的值,说明p,q在当前结点的右子树,向右遍历
    //      如果不满足上述两者,则意味着当前结点在p,q的值之间,此时这个结点就是最近的公共祖先或者就是p,q中的某一个值
    public TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {
        TreeNode ancestor = root;
        while (true) {
            if (p.val < ancestor.val && q.val < ancestor.val) {
                ancestor = ancestor.left;
            } else if (p.val > ancestor.val && q.val > ancestor.val) {
                ancestor = ancestor.right;
            } else {
                break;
            }
        }
        return ancestor;
    }
}
